        // is not restarted.
        fontFamily: 'Lato', colorScheme: ColorScheme.fromSwatch(primarySwatch: Colors.purple).copyWith(secondary: Colors.deepOrange),
      ),
      home: ProductsOverviewScreen(),
    );
  }
}
